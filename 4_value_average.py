#enter via keyboard 4 values, calculate their average
value_1 = float(input('Enter the 1st value: '))
value_2 = float(input('Enter the 2nd value: '))
value_3 = float(input('Enter the 3rd value: '))
value_4 = float(input('Enter the 4th value: '))
average = (value_1 + value_2 + value_3 + value_4) / 4
print('\nThe average of the given values is', average)